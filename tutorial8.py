# Lists and Tuples.

# We can store different data types in a list! wtf.
fruits = ['apple', 'pear', 3]

print(fruits)
fruits.append('strawberry')
fruits.append('blueberry')
print(fruits)

fruits[1] = 'katappa'
print(fruits)

