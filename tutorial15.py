# .find() and .count() functions for string.
string = 'hello'

# .find() returns the first index at which a character is found in the string.
print(string.find('o'))
print(string.find('l'))
# .find() returns -1 if the character is not found.

# .count() function counts the number of times character is present in the string.
print(string.count('l'))