# For Loops

# Range function --> First number is inclusive, second number is exclusive, last number is the step meaning the value x increases by each iteration 
for x in range(0, 10, 1):
    print(x)

# Range -> range(0, 10) means start at 0, end at 9. range(10) means start at 0 by default and end at 9.
# step is 1 by default. You can change it accordingly.