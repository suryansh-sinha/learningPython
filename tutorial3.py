# Conditions
# There are 4 main conditional operators. <, >, ==, !=.

'tim' != 'hello'    # Returns True

print(2 < 3)    # Prints True
print(2 > 3)    # Prints False
print('helo' == 'hello')    # Prints False
print('hello' == 'Hello')   # Prints False

print(True == False)    # Prints False because they're not equal